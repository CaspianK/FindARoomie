    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($room_id)
    {
        $bookmark = Bookmark::where('room_id', $room_id)->where('user_id', auth()->user()->id);
